public class arithmeticoperator {
    public static void main(String[] args) {
        int a=10;
        int b=20;

        // addition
        int sum = a+b;
        System.out.println(sum);

        //subtraction
        int diff = a-b;
        System.out.println(diff);

        // multiplication
        int mult = a*b;
        System.out.println(mult);

        //division
        float c = 1;
        float d = 2;
        float div = c/d;
        System.out.println(div);
    }
}