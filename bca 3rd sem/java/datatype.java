class datatype{
    public static void main(String[] args){
        // primitive datatype
        // byte - 1 [-128 -> 127]
        // short - 2
        // int - 4 [1,2,3,4]
        // long - 8
        // float - 4 [3.14]
        // double - 8
        // char - 2 [a,b,c,d]
        // boolean - 1 [true/false]

        byte age = 30;
        int phone = 1234567890;
        long phone2 = 123456789012L;
        float pi = 3.14F;
        char letter = '@';
        boolean isAdult = true;

        // non-primitive datatype
        // String line of charecter

        String name = "Shiva";
        System.out.println(name.length());



    }
}