public class assignmentoperator {
    public static void main(String[] args) {
        int a = 10; //"=" assign
        System.out.println(a);
        a+=5;//"+=" add and assign
        System.out.println(a);
        a-=5;//"-=" subtract and assign
        System.out.println(a);
        a/=5;//"/=" divide and assign
        System.out.println(a);
        a*=5;//"*=" multiply and assign
        System.out.println(a);
        a%=5;//"%=" modulo and assign
        System.out.println(a);
    }
}
