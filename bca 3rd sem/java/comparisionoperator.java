public class comparisionoperator {
    public static void main(String[] args) {
        // Comparision Operator
        // equal Operator(==)
        // not equal to(!=)
        // lesser than (a<b)
        // greater than (a>b)
        // less than or equal to(a<=b)
        // greater than or equal to(a>=b)

        // it always return ture or false

        int a=10;
        int b=20;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<=b);
        System.out.println(a>=b);

    }
}
