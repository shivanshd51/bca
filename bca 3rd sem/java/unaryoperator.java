public class unaryoperator {
    public static void main(String[] args) {
        // only one operand required
        
        int numb1 =1;
        System.out.println(numb1++);
        System.out.println(numb1);

        int numb2 =1;
        System.out.println(++numb2);
        System.out.println(numb2);
    }
}
