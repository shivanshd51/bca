# 1. printing name 10 times by using while loop
i=0
while(i<10):
    print("shivansh")
    i=i+1

# 2. printing number from 1 to 10
i=1
while(i<11):
    print(i)
    i+=1

# 3. printing number from 10 to 1
i=10
while(i>0):
    print(i)
    i=i-1

# 4. printing number from 10 to 1 in a single line
i=10
while(i>0):
    print(i,end=",")
    i=i-1